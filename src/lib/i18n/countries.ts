/** Localized country names where the runtime supports it (falls back to static). */
export function listCountries(displayLocale = "en"): CountryData[] {
  let names: Intl.DisplayNames | null = null;
  try {
    names = new Intl.DisplayNames([displayLocale], { type: "region" });
  } catch {
    names = null;
  }
  return COUNTRIES.map((c) => ({
    ...c,
    name: names?.of(c.code) ?? c.name,
  })).sort((a, b) => a.name.localeCompare(b.name));
}

/** Best-effort BCP-47 locale for a country (region drives number grouping). */
export function localeForCountry(countryCode: string): string {
  return `en-${countryCode.toUpperCase()}`;
}

export function currencyForCountry(countryCode: string): string | undefined {
  return COUNTRIES.find((c) => c.code === countryCode)?.currency;
}
